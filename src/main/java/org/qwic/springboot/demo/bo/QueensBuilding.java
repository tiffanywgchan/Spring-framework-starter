package org.qwic.springboot.demo.bo;

//Defines a QueenBuilding object with its attributes 
public class QueensBuilding {
	
	private String name;
	private int year;

	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getYear() {
		return year;
	}
	
	public void setYear(int year) {
		this.year = year;
	}
	
}
